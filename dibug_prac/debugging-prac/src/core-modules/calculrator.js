const calculrator = {
  addition: function (num1, num2) {
    return num1 + num2;
  },
  subtraction: function (num1, num2) {
    return num1 - num2;
  },
  multiplication: function (num1, num2) {
    return num1 * num2;
  },
  division: function (num1, num2) {
    return num1 / num2;
  },
};

export default calculrator;
